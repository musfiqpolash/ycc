    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'email'=>'required|email',
            'phone'=>'required|numeric',
            'fb'=>'nullable|active_url',
            'instagram'=>'nullable|active_url',
            'twitter'=>'nullable|active_url',
        ]);

        $info=Information::find(1);

        if (!$info) {
            $info= new Information();
        }

        $info->email=$request->email;
        $info->phone=$request->phone;
        $info->fb=$request->fb;
        $info->instagram=$request->instagramail;
        $info->twitter=$request->twitter;
        $info->save();

        return redirect()->back()->with('success', 'information added');
    }

